// 获取图片比率
function imgRatio(callback) {
  let img = new Image()
  img.src = "./img/gd.gif"
  img.onload = function() {
    // 原始图片大小
    let wt = img.width
    // 页面加载图片大小
    let waterworkimgwt = $('.water-work-img').width()
    if (waterworkimgwt >= wt) {
      $('.water-network').css('width', wt + 'px')
      callback()
    } else {
      // 获取屏幕分辨率
      let screenWidth = window.screen.width
      if (screenWidth >= 1920) {
        console.log('122', screenWidth)
        $('.water-network').css('width', '1730px')
        setMeter1()
      } else {
        $('.water-network').css('width', '1200px')
        setMeter2()
        console.log('screenWidth', screenWidth)
      }
    }
  }
}